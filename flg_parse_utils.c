int	is_terminator(const char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'u' ||\
		c == 'x' || c == 'X' || c == 'o' || c == '%')
		return (1);
	return (0);
}

int	is_flg(char c)
{
	return (c == '-' || c == ' ' || c == '+' || c == '0' || c == '#');
}
